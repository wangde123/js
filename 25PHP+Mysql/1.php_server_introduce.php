一.PHP的概述
1.PHP(超文本预处理器): 是一种免费开源的服务器端脚本程序，主要适用于Web开发领域，PHP的语法和JavaScript非常相似。
2.PHP的扩展名：.php
3.约定：文件的命名改成拼音或者英文的，服务器直接找index页面，前期少用index命名。
4.php文件内部可以写入html,css,js,但是应该做到前后端分离。反之.html文件中不能写入php代码。
5.php代码的标识符：<?php  php代码 ?> 注意：后面的?>可以省略不写。


二.操作php的注意事项 - 千万注意
1.允许拷贝上课的php代码进行修改，注意理解每一行代码的意义，学会读懂代码。
2.php代码结尾必须添加引号，除非最后一行。
3.php需要服务器预览，必须将代码放入服务器目录中，包含和php相关的前端文件也需要服务器预览。
预览方式：localhost / 127.0.0.1 (前提启动服务器)

三.php服务器介绍
1.phpStudy
phpStudy是一个PHP调试环境的程序集成包。该程序包集成最新的Apache+PHP+MySQL+phpMyAdmin，一次性安装，无须配置即可使用，是非常方便、好用的PHP调试环境。
Apache 是php服务器。
PHP 是服务器端脚本程序
MySQL 是php配合使用的数据库，Mysql支持的是命令行操作，非常麻烦，需要可视化的工具。
phpMyAdmin 是数据库操作的可视化工具，需要单独安装。

java + oracle
c# + sqlserver
php + mysql
asp + access

2.访问服务器文件或者项目(网站)：域名和ip地址都可以。
本地的ip地址：127.0.0.1
本地的域名：localhost（本地主机）

3.通信协议
注意浏览器或者盘符，隐藏协议
两台电脑要通信，必须使用通信协议http://&https://&file://&ftp://

4.端口是英文port的意译,可以认为是设备与外界通讯交流的出口。如果把服务器地址比作一间房子,端口就是出入这间房子的门。
解读：一台服务器里面如果存放多套程序，一台服务器对应一个ip地址，如何实现访问多套程序? ，通过配置端口解决
http默认端口：80
https默认端口：443
127.0.0.1:80
127.0.0.1:443


