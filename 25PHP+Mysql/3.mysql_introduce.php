三.利用phpMyadmin进行数据库和表的创建
1.创建数据库
通过用户名和密码进入数据库操作的界面,左侧提供了几个默认的数据库。
找到左侧面板[新建],右侧面板添加数据库的名称,点击创建完成即可。

2.新建数据表,一个数据库可以包含n多个表。
输入表名和字段数
字段数：根据具体的应用确定的,但必须包含一个主键,没有具体应用之前,随便编写。

3.重要的字段：
3.1.主键(一般定义为编号sid/id),也可以自由定义名称,主键一定要设置自动编号和无符号,类型可以是int或者tinyint

unsigned 无符号
primary 主键
A_I 勾上表示自动编号,自动帮我们给数据加上编号。

3.2.字符串
类型:varchar 根据自身的长度来计算占据内存长度。

如果设置密码,类型是字符串,但是长度必须是40位,密码后面要加密处理

