一.数据库
1.数据库的概念：存储数据的仓库。
数据库(database)是按照数据结构来组织、存储和管理数据的建立在计算机存储设备上的仓库。mysql是最常用的数据库类型。

2.表格--关系
mysql属于关系数据库,关系数据库由关系组成,这些关系通常称为表格。顾名思义,一个关系就是一个数据的表格。电子数据表就是一种表格。

3.列(字段)
表中的每列都有惟一的名称,包含不同的数据。此外,每一列都有一个相关的数据类型（数字,字符串,日期..)

4.行(记录)
表中的每一行代表一个客户。每一行具有相同的格式,因而也具有相同的属性。行也成为记录。

5.键(主键)
主键是唯一且不为空的,用以标识唯一的一条记录。


